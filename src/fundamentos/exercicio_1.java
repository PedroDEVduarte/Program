package fundamentos;

public class exercicio_1 {

	public static void main(String[] args) {
		// imprime Zero.
		double valor = 1/4;
		System.out.println(valor);
		
		// imprime ponto flutuante.
		valor = 1.0/4;
		System.out.println(valor);
		
		// pre e pós Incremento.
		int numero = 10;
		System.out.println(numero);
		System.out.println(numero++);
		System.out.println(++numero);
		
		char letra1 = 65; //A
		char letra2 = 97; //a
		System.out.println(letra1 + letra2);
		System.out.println("nome: " + letra1 + letra2);


	}

}
